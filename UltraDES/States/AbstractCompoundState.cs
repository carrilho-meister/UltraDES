using System;

namespace UltraDES
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   (Serializable)a abstract compound state. </summary>
    /// <remarks>   Lucas Alves, 05/01/2016. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    [Serializable]
    public abstract class AbstractCompoundState : AbstractState
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the s 1. </summary>
        /// <value> The s 1. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public abstract AbstractState S1 { get; protected set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the s 2. </summary>
        /// <value> The s 2. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        public abstract AbstractState S2 { get; protected set; }
    }
}